#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int				i;
	unsigned char	*str;

	str = s;
	i = 0;
	while (n > 0)
	{
		str[i++] = c;
		n--;
	}
	return (s);
}
